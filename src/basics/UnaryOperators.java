package basics;

public class UnaryOperators {
    public static void main(String[] args) {
        // Unary Operator in Java ++ --
        int a=10;
        System.out.println(a); //10
        a++;
        System.out.println(a); //11
        System.out.println(a++); //11
        System.out.println(a); //12
        System.out.println(++a); //13
        System.out.println(a); //13
        System.out.println(a--); //13
        System.out.println(a); //12
        System.out.println(--a); //11
        System.out.println(a); //11
    }
}
